#include "common/sample_buffer.h"

namespace psynth
{

size_t sample_buffer::find_hill (size_t start) const
{
    ++start;
    
    while (start < m_size &&
	   (m_data[start-1] != 0.0f ||
	    m_data[start] == 0.0f))
	++start;
    
    return start;
}

} /* namespace psynth */
